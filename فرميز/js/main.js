window.sendChat = async function() {
    const input = document.getElementById('chatInput');
    const body = document.getElementById('chatBody');
    if(!input || !body || !input.value.trim()) return;

    const userText = input.value.trim();
    input.value = '';

    // Track Session Context
    if(!window.chatSession) window.chatSession = [];
    window.chatSession.push({ role: 'user', content: userText, timestamp: new Date() });

    // Append user message
    const userMsg = document.createElement('div');
    userMsg.className = 'msg user-msg';
    userMsg.innerHTML = `<p>${userText}</p>`;
    body.appendChild(userMsg);
    body.scrollTop = body.scrollHeight;

    // AI thinking effect
    const thinkingId = 'ai-thinking-' + Date.now();
    const aiMsg = document.createElement('div');
    aiMsg.className = 'msg ai-msg';
    aiMsg.id = thinkingId;
    aiMsg.innerHTML = `<p>...</p>`;
    body.appendChild(aiMsg);
    body.scrollTop = body.scrollHeight;

    setTimeout(async () => {
        const aiMsgBox = document.getElementById(thinkingId);
        const response = await getAIResponse(userText);
        
        let aiText = typeof response === 'string' ? response : (response.answer || "عفواً، لم أستطع الرد.");
        let mediaHtml = '';
        
        // Handle Rich/Media content in AI response
        if(typeof response === 'object' && response.media_url) {
            mediaHtml = `<img src="${response.media_url}" style="width:100%; border-radius:10px; margin-top:10px; cursor:pointer;" onclick="window.open('${response.media_url}', '_blank')">`;
        }

        window.chatSession.push({ role: 'ai', content: aiText, media: response.media_url || null, timestamp: new Date() });
        
        if(aiMsgBox) aiMsgBox.innerHTML = `<p>${aiText}</p>${mediaHtml}`;
        body.scrollTop = body.scrollHeight;
    }, 600);
};

