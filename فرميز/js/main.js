// --- Drag to Scroll Utility ---
function makeDraggable(slider) {
    if(!slider) return;
    let isDown = false;
    let startX;
    let scrollLeft;
    let isDragging = false;

    slider.addEventListener('mousedown', (e) => {
        isDown = true;
        isDragging = false;
        slider.classList.add('active-drag');
        startX = e.pageX - slider.offsetLeft;
        scrollLeft = slider.scrollLeft;
    });

    slider.addEventListener('mouseleave', () => {
        isDown = false;
        slider.classList.remove('active-drag');
    });

    slider.addEventListener('mouseup', () => {
        isDown = false;
        slider.classList.remove('active-drag');
        if (isDragging) {
            setTimeout(() => isDragging = false, 0);
        }
    });

    slider.addEventListener('mousemove', (e) => {
        if (!isDown) return;
        e.preventDefault();
        const x = e.pageX - slider.offsetLeft;
        const walk = (x - startX) * 1.5; 
        if (Math.abs(walk) > 5) isDragging = true;
        if (isDragging) {
            slider.scrollLeft = scrollLeft - walk;
        }
    });

    // --- Touch Events for Mobile ---
    slider.addEventListener('touchstart', (e) => {
        isDown = true;
        isDragging = false;
        startX = e.touches[0].pageX - slider.offsetLeft;
        scrollLeft = slider.scrollLeft;
    }, {passive: true});

    slider.addEventListener('touchend', () => {
        isDown = false;
    }, {passive: true});

    slider.addEventListener('touchmove', (e) => {
        if (!isDown) return;
        const x = e.touches[0].pageX - slider.offsetLeft;
        const walk = (x - startX) * 1.5;
        if (Math.abs(walk) > 5) isDragging = true;
        if (isDragging) {
            slider.scrollLeft = scrollLeft - walk;
        }
    }, {passive: true});
    
    // Prevent click on cards if we were dragging
    slider.addEventListener('click', (e) => {
        if (isDragging) {
            e.preventDefault();
            e.stopPropagation();
        }
    }, true);
}

document.addEventListener('DOMContentLoaded', () => {
    document.querySelectorAll('.works-slider').forEach(makeDraggable);
});

// --- Collapsible Sections Toggle ---
window.toggleCeleb = function() {
    const grid = document.getElementById('celebHomeGrid');
    const header = grid ? grid.previousElementSibling : null;
    if (grid && header) {
        grid.classList.toggle('expanded');
        header.classList.toggle('active-toggle');
        
        // Wait for CSS transition to finish, then set to visible to allow hover effects to burst out
        if (grid.classList.contains('expanded')) {
            setTimeout(() => {
                if (grid.classList.contains('expanded')) {
                    grid.style.overflow = 'visible';
                }
            }, 800); // matches the 0.8s CSS transition
        } else {
            grid.style.overflow = 'hidden';
        }
    }
};

