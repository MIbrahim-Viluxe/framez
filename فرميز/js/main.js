function applyLang(lang) {
    currentLang = lang;
    localStorage.setItem('fz_lang', lang);
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
    
    document.querySelectorAll('[data-t]').forEach(el => { 
        if (translations[lang] && translations[lang][el.dataset.t]) {
            el.innerText = translations[lang][el.dataset.t]; 
        }
    });

    // Dynamic Placeholders
    const f = document.getElementById('contactForm');
    if(f) {
        const nameInput = f.querySelector('[name="name"]');
        if(nameInput) nameInput.placeholder = lang === 'ar' ? 'أدخل اسمك' : 'Enter your name';
        const emailInput = f.querySelector('[name="email"]');
        if(emailInput) emailInput.placeholder = lang === 'ar' ? 'أدخل بريدك' : 'Enter your email';
        const messageInput = f.querySelector('[name="message"]');
        if(messageInput) messageInput.placeholder = lang === 'ar' ? 'كيف يمكننا مساعدتك؟' : 'How can we help?';
    }
    
    const aiInput = document.getElementById('chatInput');
    if(aiInput) aiInput.placeholder = translations[lang]['aiInputPlaceholder'];
}

function toggleLang(e) { 
    if(e) e.preventDefault(); 
    const next = currentLang === 'ar' ? 'en' : 'ar';
    applyLang(next);
    location.reload(); 
}
window.toggleLang = toggleLang;

function toggleMobileMenu() {
    const nav = document.querySelector('.nav-links');
    const icon = document.querySelector('.mobile-toggle i');
    if(nav) {
        nav.classList.toggle('active');
        if (nav.classList.contains('active')) {
            if (icon) { icon.classList.remove('fa-bars-staggered'); icon.classList.add('fa-xmark'); }
            document.body.style.overflow = 'hidden';
        } else {
            if (icon) { icon.classList.remove('fa-xmark'); icon.classList.add('fa-bars-staggered'); }
            document.body.style.overflow = '';
        }
    }
}
window.toggleMobileMenu = toggleMobileMenu;

// Close menu when clicking links
document.addEventListener('click', (e) => {
    if(e.target.closest('.nav-links a')) {
        const nav = document.querySelector('.nav-links');
        const icon = document.querySelector('.mobile-toggle i');
        if (nav) nav.classList.remove('active');
        if (icon) { icon.classList.remove('fa-xmark'); icon.classList.add('fa-bars-staggered'); }
        document.body.style.overflow = '';
    }
});

